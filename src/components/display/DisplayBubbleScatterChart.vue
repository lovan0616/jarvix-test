      const hasOutlier = dummyDataSet.outliersBuckets && dummyDataSet.outliersBuckets.length > 0
      const colorAmountNeeded = hasOutlier ? dummyDataSet.columns.length + 1 : dummyDataSet.columns.length
      let colorList
