  computed: {
    colorList () {
      // Ë®≠ÂÆöÈÄèÊòéÂ∫¶ËÆì scatter chart ‰∏≠ÈáçÁñäÁöÑÈªûËÉΩË¢´Âá∏È°ØÂá∫‰æÜ
      const opacity = 0.7
